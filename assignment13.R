# Operatins on matrix
mat1<-matrix(c(1:6),nrow=3,byrow=TRUE)
mat2<-matrix(c(5:10),nrow=3,byrow=TRUE)
add<-mat1+mat2
sub<-mat1-mat2
mul<-mat1*mat2
div<-mat1/mat2
print("Addition Result")
print(add)
print("Subtraction Result")
print(sub)
print("Multiplication Result")
print(mul)
print("Division Result")
print(div)
